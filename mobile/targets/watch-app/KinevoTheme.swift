import SwiftUI

// MARK: - Kinevo Color System

extension Color {
    /// Initialize Color from hex string
    init(hex: String) {
        let hex = hex.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
        var int: UInt64 = 0
        Scanner(string: hex).scanHexInt64(&int)
        let a, r, g, b: UInt64
        switch hex.count {
        case 3: // RGB (12-bit)
            (a, r, g, b) = (255, (int >> 8) * 17, (int >> 4 & 0xF) * 17, (int & 0xF) * 17)
        case 6: // RGB (24-bit)
            (a, r, g, b) = (255, int >> 16, int >> 8 & 0xFF, int & 0xFF)
        case 8: // ARGB (32-bit)
            (a, r, g, b) = (int >> 24, int >> 16 & 0xFF, int >> 8 & 0xFF, int & 0xFF)
        default:
            (a, r, g, b) = (255, 0, 0, 0)
        }
        self.init(
            .sRGB,
            red: Double(r) / 255,
            green: Double(g) / 255,
            blue: Double(b) / 255,
            opacity: Double(a) / 255
        )
    }

    // MARK: - Kinevo Brand Colors

    static let kinevoBg = Color(hex: "0D0D17")
    static let kinevoCard = Color(hex: "1A1A2E")
    static let kinevoViolet = Color(hex: "7C3AED")
    static let kinevoVioletLight = Color(hex: "8B5CF6")

    static let kinevoTextPrimary = Color(hex: "F1F5F9")
    static let kinevoTextSecondary = Color(hex: "64748B")
    static let kinevoSuccess = Color.green
}
